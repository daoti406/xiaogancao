/**
 * 获取体质基础方案
 */
export const getConstitutionPlan = (constitutionType) => {
  return constitutionPlans[constitutionType] || constitutionPlans['平和质'];
};

/**
 * 获取性别建议
 */
export const getGenderAdvice = (gender) => {
  return genderAdvice[gender] || {};
};

/**
 * 获取特殊时期建议
 */
export const getSpecialPeriodAdvice = (period) => {
  return specialPeriodAdvice[period] || null;
};

/**
 * 获取健康问题调整建议
 */
export const getHealthIssueAdvice = (issue) => {
  return healthIssues[issue] || null;
};

/**
 * 生成完整方案
 */
export const generatePlan = ({
  constitutionType,
  gender,
  specialPeriod,
  healthIssues: userHealthIssues = []
}) => {
  // 1. 获取基础方案
  let plan = { ...getConstitutionPlan(constitutionType) };

  // 2. 添加性别建议
  const genderAdvice = getGenderAdvice(gender);
  if (genderAdvice.饮食) {
    plan.饮食 = [...plan.饮食, ...genderAdvice.饮食];
  }
  if (genderAdvice.运动) {
    plan.运动 = [...plan.运动, ...genderAdvice.运动];
  }
  if (genderAdvice.作息) {
    plan.作息 = [...plan.作息, ...genderAdvice.作息];
  }

  // 3. 添加特殊时期建议
  if (specialPeriod && specialPeriod !== 'none') {
    const periodAdvice = getSpecialPeriodAdvice(specialPeriod);
    if (periodAdvice) {
      plan.特殊时期 = periodAdvice.名称;
      if (periodAdvice.饮食) {
        plan.饮食 = [...plan.饮食, ...periodAdvice.饮食];
      }
      if (periodAdvice.运动) {
        plan.运动 = [...plan.运动, ...periodAdvice.运动];
      }
      if (periodAdvice.作息) {
        plan.作息 = [...plan.作息, ...periodAdvice.作息];
      }
      if (periodAdvice.注意) {
        plan.注意事项 = [periodAdvice.注意];
      }
    }
  }

  // 4. 添加健康问题调整
  if (userHealthIssues.length > 0) {
    const adjustments = [];
    userHealthIssues.forEach(issue => {
      const advice = getHealthIssueAdvice(issue);
      if (advice) {
        adjustments.push({ 问题: issue, ...advice });
        if (advice.饮食) {
          plan.饮食 = [...plan.饮食, ...advice.饮食];
        }
        if (advice.运动) {
          plan.运动 = [...plan.运动, ...advice.运动];
        }
        if (advice.作息) {
          plan.作息 = [...plan.作息, ...advice.作息];
        }
        if (advice.穴位) {
          plan.穴位 = [...(plan.穴位 || []), ...advice.穴位];
        }
      }
    });
    plan.健康调整 = adjustments;
  }

  // 5. 添加方案元信息
  plan._meta = {
    体质: constitutionType,
    性别: gender,
    特殊时期: specialPeriod || '无',
    生成时间: new Date().toISOString()
  };

  return plan;
};

export default {
  getConstitutionPlan,
  getGenderAdvice,
  getSpecialPeriodAdvice,
  getHealthIssueAdvice,
  generatePlan
};
