/**
 * 生成个性化养生方案
 */
export const generatePlan = ({ constitutionType, gender = 'male', specialPeriod = 'none', healthIssues = [] }) => {
  // 获取基础方案
  const basePlan = constitutionPlans[constitutionType] || constitutionPlans['平和质'];

  // 获取性别建议
  const genderAdjustment = genderAdjustments[gender] || genderAdjustments.male;

  // 获取特殊时期建议
  const periodAdjustment = specialPeriodAdjustments[specialPeriod] || specialPeriodAdjustments.none;

  // 合并基础方案和调整
  const plan = {
    体质: constitutionType || '平和质',
    体质特征: basePlan.特征,
    体质描述: basePlan.体质描述,
    饮食: basePlan.饮食 ? {
      原则: basePlan.饮食.原则 || '',
      宜食: basePlan.饮食.宜食 || [],
      忌食: basePlan.饮食.忌食 || [],
      四季调养: basePlan.饮食.四季调养 || {},
      食疗方: basePlan.饮食.食疗方 || []
    } : { 原则: '', 宜食: [], 忌食: [], 四季调养: {}, 食疗方: [] },
    运动: basePlan.运动 ? {
      原则: basePlan.运动.原则 || '',
      推荐: basePlan.运动.推荐 || [],
      时间: basePlan.运动.时间 || '',
      注意: basePlan.运动.注意 || []
    } : { 原则: '', 推荐: [], 时间: '', 注意: [] },
    作息: basePlan.作息 ? {
      原则: basePlan.作息.原则 || '',
      睡眠: basePlan.作息.睡眠 || '',
      四季调整: basePlan.作息.四季调整 || {},
      注意: basePlan.作息.注意 || []
    } : { 原则: '', 睡眠: '', 四季调整: {}, 注意: [] },
    穴位: basePlan.穴位 || [],
    情志: basePlan.情志 || '',
    保健: Array.isArray(basePlan.保健) ? basePlan.保健 : [basePlan.保健].filter(Boolean)
  };

  // 添加性别建议
  if (genderAdjustment) {
    if (genderAdjustment.饮食) {
      plan.饮食.忌食 = [...plan.饮食.忌食, genderAdjustment.饮食];
    }
    if (genderAdjustment.运动) {
      plan.运动.注意 = [...(plan.运动.注意 || []), genderAdjustment.运动];
    }
    if (genderAdjustment.作息) {
      plan.作息.注意 = [...(plan.作息.注意 || []), genderAdjustment.作息];
    }
  }

  // 添加特殊时期建议
  if (periodAdjustment && specialPeriod !== 'none') {
    plan.特殊时期 = periodAdjustment.名称;
    if (periodAdjustment.饮食) {
      plan.饮食.宜食 = [...plan.饮食.宜食, ...periodAdjustment.饮食];
    }
    if (periodAdjustment.运动) {
      plan.运动.推荐 = [...(plan.运动.推荐 || []), ...periodAdjustment.运动];
    }
    if (periodAdjustment.注意事项) {
      plan.注意事项 = periodAdjustment.注意事项;
    }
  }

  // 根据健康问题调整
  if (healthIssues && healthIssues.length > 0) {
    for (const issue of healthIssues) {
      const adjustment = healthIssueAdjustments[issue];
      if (adjustment) {
        if (adjustment.饮食) plan.饮食.宜食 = [...plan.饮食.宜食, ...adjustment.饮食];
        if (adjustment.运动) plan.运动.推荐 = [...(plan.运动.推荐 || []), ...adjustment.运动];
        if (adjustment.穴位) plan.穴位 = [...(plan.穴位 || []), ...adjustment.穴位];
      }
    }
  }

  return plan;
};

/**
 * 获取体质信息
 */
export const getConstitutionInfo = (type) => {
  return constitutionPlans[type] || constitutionPlans['平和质'];
};

export default {
  generatePlan,
  getConstitutionInfo
};
